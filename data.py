def _closed(pts):
    """Return a closed GeoJSON ring: list of [lon, lat] with first == last."""
    ring = [[lon, lat] for lon, lat in pts]
    ring.append(ring[0])
    return ring

def _feature_poly(name, rings):
    """Single polygon feature. rings = list of coordinate lists (lon, lat)."""
    return {
        "type": "Feature", "id": name,
        "geometry": {"type": "Polygon", "coordinates": [_closed(r) for r in rings]},
        "properties": {},
    }

def _feature_multi(name, rings):
    """MultiPolygon feature for geographically separate sub-areas."""
    return {
        "type": "Feature", "id": name,
        "geometry": {"type": "MultiPolygon", "coordinates": [[_closed(r)] for r in rings]},
        "properties": {},
    }

def _box(name, lon0, lat0, lon1, lat1):
    """Approximate rectangular feature for cities outside the Madison dataset."""
    return _feature_poly(name, [[(lon0, lat0), (lon1, lat0), (lon1, lat1), (lon0, lat1)]])

